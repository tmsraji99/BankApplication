package com.ojas.rpo.security.entity;

public class Deduction {

	private double employeePf;

	private double employerPf;

	private double foodCard;

	private double professionalTax;

	private double projectAllowance;

	private double locationAllowance;

	private double perMonthDeduction;

	public double getEmployeePf() {
		return employeePf;
	}

	public void setEmployeePf(double employeePf) {
		this.employeePf = employeePf;
	}

	public double getEmployerPf() {
		return employerPf;
	}

	public void setEmployerPf(double employerPf) {
		this.employerPf = employerPf;
	}

	public double getFoodCard() {
		return foodCard;
	}

	public void setFoodCard(double foodCard) {
		this.foodCard = foodCard;
	}

	public double getProfessionalTax() {
		return professionalTax;
	}

	public void setProfessionalTax(double professionalTax) {
		this.professionalTax = professionalTax;
	}

	public double getProjectAllowance() {
		return projectAllowance;
	}

	public void setProjectAllowance(double projectAllowance) {
		this.projectAllowance = projectAllowance;
	}

	public double getLocationAllowance() {
		return locationAllowance;
	}

	public void setLocationAllowance(double locationAllowance) {
		this.locationAllowance = locationAllowance;
	}

	public double getPerMonthDeduction() {
		return perMonthDeduction;
	}

	public void setPerMonthDeduction(double perMonthDeduction) {
		this.perMonthDeduction = perMonthDeduction;
	}

}
